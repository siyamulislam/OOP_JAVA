
package Calculator;


public class Calculation {
    public int sum(int a,int b){
        return a+b;
    }
    
    public int sb(int a,int b){
        return a-b;
    }
    
    public int mul(int a,int b){
        return a*b; 
    }
    
    public int div(int a,int b){
        return a/b;  
    }
    
    
}
